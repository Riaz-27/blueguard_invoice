class CustomerInfo {
  final String firstName;
  final String lastName;
  final String street;
  final String city;
  final String province;
  final String postalCode;
  final String email;
  final String phone;

  const CustomerInfo({
    required this.firstName,
    required this.lastName,
    required this.street,
    required this.city,
    required this.province,
    required this.postalCode,
    required this.email,
    required this.phone,
  });
}
